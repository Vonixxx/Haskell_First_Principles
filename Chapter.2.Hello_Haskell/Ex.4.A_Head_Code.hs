-------
-- 1 --
------------------
-- let x = 3;   --
--     y = 1000 --
-- in x * 3 + y --
------------------

one :: Num a => a
one = x * 3 + y
      where x = 3
            y = 1000

-- λ> one
-- λ> 1009

-------
-- 2 --
------------------------
-- let y = 10;        --
--     x = 10 * 5 + y --
-- in x * 5           --
------------------------

two :: Num a => a
two = x * 5
      where y = 10
            x = 10 * 5 + y

-- λ> two
-- λ> 300

-------
-- 3 --
----------------------
-- let x = 7;       --
--     z = y * 10;  --
--     y = negate x --
-- in z / x + y     --
----------------------

three :: Fractional a => a
three = z / x + y
        where x = 7
              z = y * 10
              y = negate x

-- λ> three
-- λ> -17
